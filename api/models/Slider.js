const mongoose = require("mongoose");
const Schema = mongoose.Schema;
// Create Schema
const SliderSchema = new Schema({
    title: {
        type: String,
        required: true,
    },
    description: {
        type: String,
        required: true,
    },
    image: {
        type: Object,
        default: '',
    },
    created_at: {
        type: Date,
        default: Date.now
    },
    updated_at: {
        type: Date,
        default: Date.now
    },

});

module.exports = Slider = mongoose.model("slider", SliderSchema);